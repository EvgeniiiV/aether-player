#!/bin/bash

# –°–∏—Å—Ç–µ–º–∞ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –ø–∏—Ç–∞–Ω–∏–µ–º –ø–µ—Ä–∏—Ñ–µ—Ä–∏–∏ Aether Player (v4.0)
# –£–ø—Ä–∞–≤–ª—è–µ—Ç —Ä–µ–ª–µ 220–í —á–µ—Ä–µ–∑ GPIO –¥–ª—è –≤–∫–ª—é—á–µ–Ω–∏—è/–≤—ã–∫–ª—é—á–µ–Ω–∏—è —É—Å–∏–ª–∏—Ç–µ–ª—è, HDD –∏ –¥—Ä—É–≥–æ–π –ø–µ—Ä–∏—Ñ–µ—Ä–∏–∏
# –ò—Å–ø–æ–ª—å–∑—É–µ—Ç libgpiod —Å —Ñ–æ–Ω–æ–≤—ã–º –ø—Ä–æ—Ü–µ—Å—Å–æ–º –¥–ª—è —É–¥–µ—Ä–∂–∞–Ω–∏—è —Å–æ—Å—Ç–æ—è–Ω–∏—è

# –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è
POWER_GPIO=18
GPIO_CHIP="gpiochip0"
PIDFILE="/tmp/aether-power-gpio.pid"

# –§—É–Ω–∫—Ü–∏—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å–æ—Å—Ç–æ—è–Ω–∏—è GPIO
check_gpio() {
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —á–µ—Ä–µ–∑ gpioget
    timeout 2 gpioget $GPIO_CHIP $POWER_GPIO 2>/dev/null
}

# –§—É–Ω–∫—Ü–∏—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –∞–∫—Ç–∏–≤–Ω–æ–≥–æ –ø—Ä–æ—Ü–µ—Å—Å–∞ GPIO
is_gpio_active() {
    if [ -f "$PIDFILE" ]; then
        PID=$(cat "$PIDFILE")
        if ps -p "$PID" > /dev/null 2>&1; then
            return 0  # –ü—Ä–æ—Ü–µ—Å—Å –∞–∫—Ç–∏–≤–µ–Ω
        else
            # –§–∞–π–ª PID —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –Ω–æ –ø—Ä–æ—Ü–µ—Å—Å –º–µ—Ä—Ç–≤
            rm -f "$PIDFILE"
            return 1
        fi
    fi
    return 1  # –§–∞–π–ª PID –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
}

# –§—É–Ω–∫—Ü–∏—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏ GPIO –ø—Ä–æ—Ü–µ—Å—Å–∞
stop_gpio_process() {
    if [ -f "$PIDFILE" ]; then
        PID=$(cat "$PIDFILE")
        if ps -p "$PID" > /dev/null 2>&1; then
            echo "üõë –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ø—Ä–æ—Ü–µ—Å—Å GPIO (PID: $PID)..."
            kill "$PID" 2>/dev/null
            sleep 0.5
        fi
        rm -f "$PIDFILE"
    fi
}

# –§—É–Ω–∫—Ü–∏—è –≤–∫–ª—é—á–µ–Ω–∏—è –ø–∏—Ç–∞–Ω–∏—è —Å —É–¥–µ—Ä–∂–∞–Ω–∏–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏—è
power_on() {
    echo "‚ö° –í–∫–ª—é—á–µ–Ω–∏–µ –ø–∏—Ç–∞–Ω–∏—è –ø–µ—Ä–∏—Ñ–µ—Ä–∏–∏..."
    
    # –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π –ø—Ä–æ—Ü–µ—Å—Å –µ—Å–ª–∏ –µ—Å—Ç—å
    stop_gpio_process
    
    # –ó–∞–ø—É—Å–∫–∞–µ–º —Ñ–æ–Ω–æ–≤—ã–π –ø—Ä–æ—Ü–µ—Å—Å –¥–ª—è —É–¥–µ—Ä–∂–∞–Ω–∏—è HIGH
    echo "üîß –ó–∞–ø—É—Å–∫ —Ñ–æ–Ω–æ–≤–æ–≥–æ –ø—Ä–æ—Ü–µ—Å—Å–∞ GPIO..."
    nohup gpioset --mode=signal $GPIO_CHIP $POWER_GPIO=1 > /dev/null 2>&1 &
    GPIO_PID=$!
    echo "$GPIO_PID" > "$PIDFILE"
    
    sleep 0.5
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ
    STATE=$(check_gpio)
    if [ "$STATE" = "1" ] && is_gpio_active; then
        echo "‚úÖ –ü–∏—Ç–∞–Ω–∏–µ –ø–µ—Ä–∏—Ñ–µ—Ä–∏–∏ –≤–∫–ª—é—á–µ–Ω–æ"
        echo "üìä –°–æ—Å—Ç–æ—è–Ω–∏–µ —Ä–µ–ª–µ: –í–ö–õ–Æ–ß–ï–ù–û (GPIO $POWER_GPIO = HIGH)"
        echo "üîÑ –§–æ–Ω–æ–≤—ã–π –ø—Ä–æ—Ü–µ—Å—Å: PID $GPIO_PID"
        logger "Aether Player: –ü–∏—Ç–∞–Ω–∏–µ –ø–µ—Ä–∏—Ñ–µ—Ä–∏–∏ –≤–∫–ª—é—á–µ–Ω–æ (PID: $GPIO_PID)"
    else
        echo "‚ùå –û—à–∏–±–∫–∞: –ù–µ —É–¥–∞–ª–æ—Å—å –≤–∫–ª—é—á–∏—Ç—å –ø–∏—Ç–∞–Ω–∏–µ"
        echo "üìä GPIO $POWER_GPIO = $STATE"
        stop_gpio_process
        return 1
    fi
}

# –§—É–Ω–∫—Ü–∏—è –≤—ã–∫–ª—é—á–µ–Ω–∏—è –ø–∏—Ç–∞–Ω–∏—è
power_off() {
    echo "‚ö†Ô∏è –í—ã–∫–ª—é—á–µ–Ω–∏–µ –ø–∏—Ç–∞–Ω–∏—è –ø–µ—Ä–∏—Ñ–µ—Ä–∏–∏..."
    
    # –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ø—Ä–æ—Ü–µ—Å—Å GPIO (—ç—Ç–æ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ —É—Å—Ç–∞–Ω–æ–≤–∏—Ç LOW)
    stop_gpio_process
    
    sleep 0.5
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ
    STATE=$(check_gpio)
    if [ "$STATE" = "0" ]; then
        echo "‚úÖ –ü–∏—Ç–∞–Ω–∏–µ –ø–µ—Ä–∏—Ñ–µ—Ä–∏–∏ –≤—ã–∫–ª—é—á–µ–Ω–æ"
        echo "üìä –°–æ—Å—Ç–æ—è–Ω–∏–µ —Ä–µ–ª–µ: –í–´–ö–õ–Æ–ß–ï–ù–û (GPIO $POWER_GPIO = LOW)"
        logger "Aether Player: –ü–∏—Ç–∞–Ω–∏–µ –ø–µ—Ä–∏—Ñ–µ—Ä–∏–∏ –≤—ã–∫–ª—é—á–µ–Ω–æ"
    else
        echo "‚ùå –û—à–∏–±–∫–∞: –ù–µ —É–¥–∞–ª–æ—Å—å –≤—ã–∫–ª—é—á–∏—Ç—å –ø–∏—Ç–∞–Ω–∏–µ"
        echo "üìä GPIO $POWER_GPIO = $STATE (–æ–∂–∏–¥–∞–ª–æ—Å—å 0)"
        return 1
    fi
}

# –§—É–Ω–∫—Ü–∏—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å–æ—Å—Ç–æ—è–Ω–∏—è
status() {
    echo "üìä –°–æ—Å—Ç–æ—è–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –ø–∏—Ç–∞–Ω–∏–µ–º:"
    echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
    
    STATE=$(check_gpio)
    GPIO_ACTIVE=$(is_gpio_active && echo "–¥–∞" || echo "–Ω–µ—Ç")
    
    if [ $? -eq 0 ]; then
        case "$STATE" in
            "1")
                echo "üü¢ –†–µ–ª–µ: –í–ö–õ–Æ–ß–ï–ù–û (GPIO $POWER_GPIO = HIGH)"
                echo "‚ö° –ü–µ—Ä–∏—Ñ–µ—Ä–∏—è: –ü–ò–¢–ê–ù–ò–ï –ü–û–î–ê–ù–û"
                if is_gpio_active; then
                    PID=$(cat "$PIDFILE")
                    echo "üîÑ –§–æ–Ω–æ–≤—ã–π –ø—Ä–æ—Ü–µ—Å—Å: –∞–∫—Ç–∏–≤–µ–Ω (PID: $PID)"
                else
                    echo "‚ö†Ô∏è –í–Ω–∏–º–∞–Ω–∏–µ: GPIO HIGH, –Ω–æ –ø—Ä–æ—Ü–µ—Å—Å –Ω–µ –∞–∫—Ç–∏–≤–µ–Ω"
                fi
                ;;
            "0")
                echo "üî¥ –†–µ–ª–µ: –í–´–ö–õ–Æ–ß–ï–ù–û (GPIO $POWER_GPIO = LOW)"
                echo "üö´ –ü–µ—Ä–∏—Ñ–µ—Ä–∏—è: –ü–ò–¢–ê–ù–ò–ï –û–¢–ö–õ–Æ–ß–ï–ù–û"
                if is_gpio_active; then
                    echo "‚ö†Ô∏è –í–Ω–∏–º–∞–Ω–∏–µ: –§–æ–Ω–æ–≤—ã–π –ø—Ä–æ—Ü–µ—Å—Å –∞–∫—Ç–∏–≤–µ–Ω –ø—Ä–∏ LOW —Å–æ—Å—Ç–æ—è–Ω–∏–∏"
                fi
                ;;
            *)
                echo "‚ùì –ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ: $STATE"
                ;;
        esac
    else
        echo "‚ùå –û—à–∏–±–∫–∞ —á—Ç–µ–Ω–∏—è GPIO $POWER_GPIO"
        echo "üîß –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç—å GPIO –∏–ª–∏ –ø—Ä–∞–≤–∞ –¥–æ—Å—Ç—É–ø–∞"
    fi
    
    echo ""
    echo "üîß –£–ø—Ä–∞–≤–ª—è–µ–º—ã–µ —É—Å—Ç—Ä–æ–π—Å—Ç–≤–∞:"
    echo "  ‚Ä¢ –£—Å–∏–ª–∏—Ç–µ–ª—å –∑–≤—É–∫–∞"
    echo "  ‚Ä¢ –í–Ω–µ—à–Ω–∏–π HDD"
    echo "  ‚Ä¢ –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –ø–µ—Ä–∏—Ñ–µ—Ä–∏—è"
    
    echo ""
    echo "üí° –¢–µ—Ö–Ω–∏—á–µ—Å–∫–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è:"
    echo "  üéØ GPIO —á–∏–ø: $GPIO_CHIP"
    echo "  üìå GPIO –ø–∏–Ω: $POWER_GPIO (—Ñ–∏–∑–∏—á–µ—Å–∫–∏–π pin 12)"
    echo "  üîÑ –ü—Ä–æ—Ü–µ—Å—Å –∞–∫—Ç–∏–≤–µ–Ω: $GPIO_ACTIVE"
    
    if [ -f "$PIDFILE" ]; then
        echo "  üìÑ PID —Ñ–∞–π–ª: $PIDFILE"
        echo "  üÜî –ü—Ä–æ—Ü–µ—Å—Å PID: $(cat $PIDFILE)"
    fi
}

# –§—É–Ω–∫—Ü–∏—è –±–µ–∑–æ–ø–∞—Å–Ω–æ–≥–æ –≤—ã–∫–ª—é—á–µ–Ω–∏—è —Å –æ—Ç–º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ–º HDD
safe_power_off() {
    echo "üõ°Ô∏è –ë–µ–∑–æ–ø–∞—Å–Ω–æ–µ –≤—ã–∫–ª—é—á–µ–Ω–∏–µ –ø–∏—Ç–∞–Ω–∏—è –ø–µ—Ä–∏—Ñ–µ—Ä–∏–∏..."
    
    # –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º Aether Player
    echo "üéµ –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º Aether Player..."
    sudo pkill -f "python.*app" 2>/dev/null || true
    sudo pkill -f mpv 2>/dev/null || true
    sleep 2
    
    # –û—Ç–º–æ–Ω—Ç–∏—Ä—É–µ–º HDD
    echo "üíæ –û—Ç–º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –≤–Ω–µ—à–Ω–∏—Ö –Ω–∞–∫–æ–ø–∏—Ç–µ–ª–µ–π..."
    
    # –ò—â–µ–º –ø—Ä–∏–º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ USB –Ω–∞–∫–æ–ø–∏—Ç–µ–ª–∏
    USB_MOUNTS=$(mount | grep "/dev/sd" | awk '{print $1}' | sort -u)
    if [ ! -z "$USB_MOUNTS" ]; then
        for DEVICE in $USB_MOUNTS; do
            echo "üì§ –û—Ç–º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ $DEVICE..."
            sudo umount "$DEVICE" 2>/dev/null || true
        done
        
        # –°–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏—è —Ñ–∞–π–ª–æ–≤–æ–π —Å–∏—Å—Ç–µ–º—ã
        echo "üîÑ –°–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏—è —Ñ–∞–π–ª–æ–≤–æ–π —Å–∏—Å—Ç–µ–º—ã..."
        sync
        sleep 2
    else
        echo "‚ÑπÔ∏è –í–Ω–µ—à–Ω–∏–µ –Ω–∞–∫–æ–ø–∏—Ç–µ–ª–∏ –Ω–µ –æ–±–Ω–∞—Ä—É–∂–µ–Ω—ã"
    fi
    
    # –í—ã–∫–ª—é—á–∞–µ–º –ø–∏—Ç–∞–Ω–∏–µ
    power_off
    
    echo "‚úÖ –ë–µ–∑–æ–ø–∞—Å–Ω–æ–µ –≤—ã–∫–ª—é—á–µ–Ω–∏–µ –∑–∞–≤–µ—Ä—à–µ–Ω–æ"
}

# –§—É–Ω–∫—Ü–∏—è —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è —Ä–µ–ª–µ
test_relay() {
    echo "üß™ –¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –ø–∏—Ç–∞–Ω–∏–µ–º..."
    echo ""
    
