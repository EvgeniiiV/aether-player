    gpioinfo | grep "line.*$POWER_GPIO:" || echo "‚ùå GPIO $POWER_GPIO –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω"
    echo ""
    
    echo "2Ô∏è‚É£ –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ç–µ–∫—É—â–µ–≥–æ —Å–æ—Å—Ç–æ—è–Ω–∏—è:"
    status
    echo ""
    
    echo "3Ô∏è‚É£ –¢–µ—Å—Ç –≤–∫–ª—é—á–µ–Ω–∏—è:"
    power_on
    sleep 3
    echo ""
    
    echo "4Ô∏è‚É£ –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–æ—Å—Ç–æ—è–Ω–∏—è –ø–æ—Å–ª–µ –≤–∫–ª—é—á–µ–Ω–∏—è:"
    status
    echo ""
    
    echo "5Ô∏è‚É£ –¢–µ—Å—Ç –≤—ã–∫–ª—é—á–µ–Ω–∏—è:"
    power_off
    sleep 2
    echo ""
    
    echo "6Ô∏è‚É£ –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–æ—Å—Ç–æ—è–Ω–∏—è –ø–æ—Å–ª–µ –≤—ã–∫–ª—é—á–µ–Ω–∏—è:"
    status
    echo ""
    
    echo "‚úÖ –¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –∑–∞–≤–µ—Ä—à–µ–Ω–æ"
    echo "üí° –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –º—É–ª—å—Ç–∏–º–µ—Ç—Ä–æ–º —Ä–µ–∞–ª—å–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ —Ä–µ–ª–µ"
    echo "üîß –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ —Ä–µ–ª–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–æ –∫ GPIO $POWER_GPIO (pin 12)"
}

# –§—É–Ω–∫—Ü–∏—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏
init() {
    echo "ÔøΩ –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è GPIO —Å–∏—Å—Ç–µ–º—ã..."
    echo "üì° –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Å–æ–≤—Ä–µ–º–µ–Ω–Ω–∞—è —Å–∏—Å—Ç–µ–º–∞ libgpiod"
